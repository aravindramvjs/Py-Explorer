# Bitwise operator are

# 1. complement(~)
print(~12)

# 2. And(&)
print(12 & 13)

# 3. Or(|)
print(12 | 13)

# 4. XOR(^)
print(12 ^ 13 )

# 5. Left Swift(<<)
print(10 << 2)

# 6. Right Swift(>>)
print(10 >> 2 )

